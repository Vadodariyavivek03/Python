# Tuple : Used to store multiple items in a single variable. tuple is orderd, immutable and allow duplicate value.

# Accessing values in tuple : 

tuple = (1, 2, 3, 4, 5)
print(tuple)

print(type(tuple))

# Update tuple element : 

x = list(tuple)
x[2] = 10
print(x)

# Delete tuple element : 

del tuple
print(tuple)

