#String Function

x = 'HELLO,my name is VIVEK.i am from Rajkot.'

#String Changecase function

print(x.upper()) 
print(x.lower())
print(x.title())
print(x.capitalize()) 

# It will return true or false of corresponding string. 

print(x.isupper()) 
print(x.islower())
print(x.istitle())

# String Length and Count

print(len(x)) 
print(x.count('a'))

